// Language switching functionality
class I18n {
  constructor() {
    this.currentLang = this.getStoredLanguage() || 'en';
    this.init();
  }

  init() {
    // Apply stored language on page load
    this.setLanguage(this.currentLang, false);

    // Set up language switcher buttons
    this.setupLanguageSwitcher();
  }

  getStoredLanguage() {
    return localStorage.getItem('hypeai_language');
  }

  setStoredLanguage(lang) {
    localStorage.setItem('hypeai_language', lang);
  }

  setupLanguageSwitcher() {
    // Support old format (dropdown)
    const langBtn = document.getElementById('langBtn');
    const langDropdown = document.getElementById('langDropdown');

    if (langBtn && langDropdown) {
      langBtn.addEventListener('click', (e) => {
        e.stopPropagation();
        langDropdown.classList.toggle('show');
        langBtn.setAttribute('aria-expanded', langDropdown.classList.contains('show'));
      });

      document.addEventListener('click', () => {
        langDropdown.classList.remove('show');
        langBtn.setAttribute('aria-expanded', 'false');
      });

      const enOption = document.getElementById('langEn');
      const ruOption = document.getElementById('langRu');

      if (enOption) {
        enOption.addEventListener('click', (e) => {
          e.preventDefault();
          this.setLanguage('en');
        });
      }

      if (ruOption) {
        ruOption.addEventListener('click', (e) => {
          e.preventDefault();
          this.setLanguage('ru');
        });
      }
    }

    // Support new format (toggle button - services.html)
    const langToggle = document.getElementById('lang-toggle');
    if (langToggle) {
      const langOptions = langToggle.querySelectorAll('.lang-option');

      langOptions.forEach(option => {
        option.addEventListener('click', (e) => {
          const lang = option.getAttribute('data-lang');
          this.setLanguage(lang);
        });
      });
    }
  }

  setLanguage(lang, saveToStorage = true) {
    this.currentLang = lang;

    if (saveToStorage) {
      this.setStoredLanguage(lang);
    }

    // Update HTML lang attribute
    document.documentElement.lang = lang;

    // Update all elements with data-i18n attribute
    this.updateAllTranslations();

    // Update button state
    this.updateButtonState();

    // Apply layout adjustments for Russian
    this.applyLayoutAdjustments(lang);
  }

  updateAllTranslations() {
    const elements = document.querySelectorAll('[data-i18n]');

    elements.forEach(element => {
      const key = element.getAttribute('data-i18n');

      // Handle nested keys like "nav.home" or "services.hero.label"
      const keys = key.split('.');
      let translation = translations[this.currentLang];

      for (const k of keys) {
        if (translation && translation[k]) {
          translation = translation[k];
        } else {
          translation = null;
          break;
        }
      }

      if (translation) {
        // Handle different element types
        if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
          element.placeholder = translation;
        } else {
          element.textContent = translation;
        }
      }
    });
  }

  updateButtonState() {
    // Update dropdown button with emoji
    const langBtn = document.getElementById('langBtn');
    if (langBtn) {
      langBtn.textContent = this.currentLang.toUpperCase() + ' üåê';
    }

    const enOption = document.getElementById('langEn');
    const ruOption = document.getElementById('langRu');

    if (enOption && ruOption) {
      enOption.classList.toggle('active', this.currentLang === 'en');
      ruOption.classList.toggle('active', this.currentLang === 'ru');
    }

    // Update new format toggle button (services.html)
    const langToggle = document.getElementById('lang-toggle');
    if (langToggle) {
      const langOptions = langToggle.querySelectorAll('.lang-option');
      langOptions.forEach(option => {
        const lang = option.getAttribute('data-lang');
        option.classList.toggle('active', lang === this.currentLang);
      });
    }
  }

  applyLayoutAdjustments(lang) {
    const root = document.documentElement;

    if (lang === 'ru') {
      // Russian text is typically 15-20% longer, adjust layouts
      root.style.setProperty('--lang-scale', '0.95');
      root.classList.add('lang-ru');
    } else {
      root.style.setProperty('--lang-scale', '1');
      root.classList.remove('lang-ru');
    }
  }

  translate(key) {
    return translations[this.currentLang][key] || key;
  }
}

// Initialize i18n system when DOM is ready
if (document.readyState === 'loading') {
  document.addEventListener('DOMContentLoaded', () => {
    window.i18n = new I18n();
  });
} else {
  window.i18n = new I18n();
}

// Export for use in other scripts
if (typeof module !== 'undefined' && module.exports) {
  module.exports = { translations, I18n };
}
